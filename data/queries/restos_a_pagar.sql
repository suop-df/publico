WITH HIERARQUIA AS (
    SELECT
        D.COCLASSEORC,
        D.NOCLASSIFICACAO                    AS NODESPESA,
        SUBSTR(D.COCLASSEORC, 1, 1)          AS COCATEGORIAECONOMICA,
        CE.NOCLASSIFICACAO                   AS NOCATEGORIAECONOMICA,
        SUBSTR(D.COCLASSEORC, 1, 2)          AS COGND,
        GND.NOCLASSIFICACAO                  AS NOGND
    FROM {SCHEMA_ANO}.CLASSIFICACAOORCAMENTARIA D
    LEFT JOIN {SCHEMA_ANO}.CLASSIFICACAOORCAMENTARIA CE
        ON SUBSTR(D.COCLASSEORC, 1, 1) || '0000000' = CE.COCLASSEORC
    LEFT JOIN {SCHEMA_ANO}.CLASSIFICACAOORCAMENTARIA GND
        ON SUBSTR(D.COCLASSEORC, 1, 2) || '000000'  = GND.COCLASSEORC
)
SELECT
    SC.COUG,
    UG.NOUG,
    SC.COEXERCICIO                 AS ANO,
    SC.INMES,
    SC.COCONTACONTABIL,
    H.COCLASSEORC                  AS CODESPESA,
    H.NODESPESA,
    H.COCATEGORIAECONOMICA,
    H.NOCATEGORIAECONOMICA,
    H.COGND,
    H.NOGND,
    SC.VADEBITO,
    SC.VACREDITO
FROM MIL2001.SALDOCONTABIL_EX SC
LEFT JOIN {SCHEMA_ANO}.UNIDADEGESTORA UG
    ON SC.COUG = UG.COUG
LEFT JOIN HIERARQUIA H
    ON SC.CONATUREZA || '00' = H.COCLASSEORC
WHERE SC.COCONTACONTABIL IN (
    631100000, 631200000, 631300000, 631400000,
    631810000, 631820000, 631900000,
    632110100, 632110300, 632110400,
    632210100, 632210200, 632210300, 632210400,
    632900000
)
AND SC.INMES <= 12
AND SC.COEXERCICIO > (EXTRACT(YEAR FROM SYSDATE) - 4)

